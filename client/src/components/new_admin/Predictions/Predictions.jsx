import React, { useState, useEffect, useCallback } from "react";
import axios from "axios";
import "./Predictions.css";

const API_URL = process.env.REACT_APP_API_URL || "http://localhost:3001/api";

const Predictions = () => {
  const [predictions, setPredictions] = useState([]);
  const [loading, setLoading] = useState(true);
  const [filter, setFilter] = useState("all");

  const loadPredictions = useCallback(async () => {
    try {
      setLoading(true);
      const response = await axios.get(`${API_URL}/admin/predictions`, {
        params: { filter },
      });
      setPredictions(response.data.predictions || []);
    } catch (error) {
      console.error("Failed to load predictions:", error);
    } finally {
      setLoading(false);
    }
  }, [filter]);

  useEffect(() => {
    loadPredictions();
  }, [loadPredictions]);

  const formatDate = (dateString) => {
    return new Date(dateString).toLocaleDateString("en-US", {
      year: "numeric",
      month: "short",
      day: "numeric",
    });
  };

  return (
    <div className="admin-predictions">
      <div className="predictions-header">
        <h1>Prediction History</h1>
        <div className="predictions-controls">
          <select
            value={filter}
            onChange={(e) => setFilter(e.target.value)}
            className="filter-select"
          >
            <option value="all">All Predictions</option>
            <option value="today">Today</option>
            <option value="week">This Week</option>
            <option value="month">This Month</option>
          </select>
        </div>
      </div>

      {loading ? (
        <div className="predictions-loading">
          <i className="fas fa-spinner fa-spin"></i>
          <p>Loading predictions...</p>
        </div>
      ) : (
        <div className="predictions-table-container">
          <table className="predictions-table">
            <thead>
              <tr>
                <th>Location</th>
                <th>Date</th>
                <th>Predicted Footfall</th>
                <th>Confidence</th>
                <th>Generated By</th>
              </tr>
            </thead>
            <tbody>
              {predictions.length > 0 ? (
                predictions.map((prediction) => (
                  <tr key={prediction._id}>
                    <td>{prediction.location}</td>
                    <td>{formatDate(prediction.createdAt)}</td>
                    <td>{prediction.predicted_footfall?.toLocaleString()}</td>
                    <td>{(prediction.confidence * 100).toFixed(1)}%</td>
                    <td>{prediction.user_email || "System"}</td>
                  </tr>
                ))
              ) : (
                <tr>
                  <td colSpan="5" className="no-data">
                    No predictions found
                  </td>
                </tr>
              )}
            </tbody>
          </table>
        </div>
      )}
    </div>
  );
};

export default Predictions;
